// ============================================================================
// 步骤 1：对节点 - 识别参与方
// ============================================================================

/**
 * User 节点常量
 * 
 * 代表用户交互节点，负责：
 * - 接收用户输入（消息、操作等）
 * - 展示 Agent 的响应给用户
 */
export const USER = "user";

/**
 * Agent 节点常量
 * 
 * 代表 AI Agent 节点，负责：
 * - 处理用户消息
 * - 调用 LLM 生成响应
 * - 决定是否需要调用工具
 * - 生成最终回复
 */
export const AGENT = "agent";

/**
 * Toolkit 节点常量
 * 
 * 代表工具包节点，负责：
 * - 接收 Agent 的工具调用请求
 * - 执行具体的工具操作
 * - 返回工具执行结果
 */
export const TOOLKIT = "toolkit";

/**
 * 参与者类型
 * 
 * 所有参与交互的节点类型
 */
export type Participants = typeof USER | typeof AGENT | typeof TOOLKIT;

